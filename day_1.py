# =======================================================================================================================================================
# INITIAL VARIABLES
# =======================================================================================================================================================
code = []
dial = 50 
counter = 0
# =======================================================================================================================================================

# =======================================================================================================================================================
# READ INPUT FILE
# =======================================================================================================================================================
with open("input_day1.txt", "r") as f:
    for line in f:
        code.append(line)
# =======================================================================================================================================================

# =======================================================================================================================================================
# TRANSLATE STRING COMMAND TO INTEGERS COMMAND
# =======================================================================================================================================================
def get_command(command):
    
    direction = command[0]
    length = int(command[1:])

    if direction == "R":
        direction = 1
    elif direction == "L":
        direction = -1
    else :
        print("Error direction")
        return 0

    return direction, length
# =======================================================================================================================================================

# =======================================================================================================================================================
# PART 1) 
# ============================================
def change_dial_pt1(dial, counter, direction, length):
    """
    Count everytime dial stops on 0
    """
    dial += direction*length
    dial = dial % 100
    if dial == 0:
        counter += 1
    return dial, counter

# =======================================================================================================================================================
# PART 2) 
# =======================================================================================================================================================
def change_dial_pt2(dial, counter, direction, length):
    """
    Count everytime dial pass through 0
    """
    for i in range(length):  
        dial = (dial + direction) % 100
        if dial == 0:
            counter += 1
    return dial, counter


# =======================================================================================================================================================
# Main loop
# =======================================================================================================================================================
for command in code :
    direction, length =  get_command(command)
    dial, counter = change_dial_pt2(dial, counter, direction, length)

print(counter)